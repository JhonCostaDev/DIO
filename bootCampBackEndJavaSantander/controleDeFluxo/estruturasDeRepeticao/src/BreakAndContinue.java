public class BreakAndContinue {
    public static void main(String[] args) {
        //Break: interromper o laço de repetição
        for(int i = 0; i <= 10; i++) {
            if (i == 6) {
                //break;
                continue;
            }
            System.out.println(i);
        }

        // Continue: 
    }
}
